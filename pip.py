import camelcase #instalamos el modulo camelcase desde la terminal

camel = camelcase.CamelCase()#creamos un objeto llamado camel que es igual numbre del modulo.creamos una clase
texto = "hola mi nobre es gera" #agregamos un valor al objeto camel

print(camel.hump(texto))#el modulo convierte las primeras letras de cada palabra del texto en mayusculas con la funcion hump

